<p>Uterine fibroids are non-cancerous growths of the uterus that often appear during childbearing years. Many women have fibroids without ever knowing — but for others, they cause significant disruption to daily life. Knowing the warning signs is the first step to getting the right care.</p>
<div class="post-callout"><p>"Many women suffer in silence for years, assuming heavy periods or pelvic pain are normal. They are not. If these symptoms are affecting your life, please come in — most fibroids are very treatable." — Dr. Ankita Bansal Goyal</p></div>
<h2 id="what-are-fibroids">What Are Uterine Fibroids?</h2>
<p>Fibroids are benign (non-cancerous) tumours that grow within the muscular wall of the uterus. They range from the size of a pea to a melon and can be single or multiple. Though the exact cause is unknown, estrogen and progesterone appear to promote fibroid growth.</p>
<h3 id="sign-1">Sign 1: Heavy or Prolonged Menstrual Bleeding</h3>
<p>Soaking through pads or tampons every hour, passing large clots, or periods lasting longer than 7 days are classic fibroid symptoms. This can lead to anaemia, causing fatigue and shortness of breath.</p>
<h3 id="sign-2">Sign 2: Pelvic Pain or Pressure</h3>
<p>A constant feeling of heaviness, pressure, or fullness in the lower abdomen — especially between periods — is a hallmark symptom of larger fibroids pressing against surrounding structures.</p>
<h3 id="sign-3">Sign 3: Frequent or Difficult Urination</h3>
<p>Fibroids pressing on the bladder can cause an urgent or frequent need to urinate, or difficulty fully emptying the bladder — especially troublesome at night.</p>
<h3 id="sign-4">Sign 4: Lower Back or Leg Pain</h3>
<p>Fibroids growing towards the back of the uterus can press on spinal nerves, causing persistent lower back pain or pain that radiates into the legs.</p>
<h3 id="sign-5">Sign 5: Pain During Intercourse</h3>
<p>Depending on the location and size of the fibroid, sexual intercourse can become painful or deeply uncomfortable.</p>
<h3 id="sign-6">Sign 6: Constipation or Bloating</h3>
<p>Fibroids pressing on the rectum can cause constipation, a feeling of rectal pressure, or persistent abdominal bloating unrelated to diet.</p>
<h3 id="sign-7">Sign 7: Enlarged or Distended Abdomen</h3>
<p>A noticeably swollen lower abdomen that cannot be explained by weight gain may be caused by a large fibroid or multiple fibroids increasing the size of the uterus.</p>
<h2 id="treatment">Treatment Options</h2>
<p>Treatment depends on the size, location, symptoms, and your fertility goals. Options range from watchful waiting and hormonal medications to laparoscopic myomectomy (removing fibroids while preserving the uterus) or hysterectomy for severe cases. Dr. Ankita prioritises fertility-preserving approaches wherever possible.</p>';
