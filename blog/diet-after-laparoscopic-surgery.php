<h2 id="immediate">Immediate Diet (First 24-48 Hours)</h2>
<p>Because anesthesia slows down your digestive tract, starting slow is crucial:</p>
<ul>
    <li><strong>Clear Fluids First:</strong> Start with water, clear broths, and light teas immediately after surgery.</li>
    <li><strong>Soft Foods Next:</strong> Once you are passing gas (a sign your bowels are waking up), transition to soft foods like yogurt, applesauce, plain toast, and dal (lentils).</li>
</ul>

<h2 id="what-to-eat">What to Eat (Healing Foods)</h2>
<p>Once you are back to a normal diet, focus on foods that promote tissue repair and reduce inflammation:</p>
<ul>
    <li><strong>High-Fiber Foods:</strong> Constipation is a common side effect of pain medication. Eat plenty of oats, whole grains, beans, and fresh fruits to keep your bowels moving gently.</li>
    <li><strong>Lean Proteins:</strong> Protein is the building block of healing tissue. Include eggs, fish, chicken, paneer, and lentils in your daily meals.</li>
    <li><strong>Vitamin C & Zinc:</strong> Citrus fruits, berries, spinach, and nuts help repair the small incisions made during your <a href="../gynecological-surgery-in-ambikapur.php" style="color:#ee7c31; font-weight: 600; text-decoration: underline;">expert gynecological surgery</a>.</li>
    <li><strong>Hydration:</strong> Drink at least 8 to 10 glasses of water daily. Proper hydration softens stools and speeds up the removal of anesthesia from your system.</li>
</ul>

<h2 id="what-to-avoid">What to Avoid</h2>
<ul>
    <li><strong>Gas-Producing Foods:</strong> Avoid cabbage, broccoli, cauliflower, carbonated drinks, and chewing gum to prevent worsening post-surgical gas pain.</li>
    <li><strong>Refined Sugars & Deep-Fried Foods:</strong> These can drastically increase inflammation and slow down the healing process.</li>
