<p>Endometriosis affects an estimated <strong>190 million women worldwide</strong>, yet on average takes <strong>7–10 years</strong> to diagnose. It is one of the most under-recognised and under-treated conditions in women\'s health. Early detection changes everything — from pain management to fertility outcomes.</p>
