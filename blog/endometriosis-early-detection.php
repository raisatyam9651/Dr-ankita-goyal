<h2 id="what-is-endo">What Is Endometriosis?</h2>
<p>Endometriosis is a chronic condition where tissue similar to the uterine lining (endometrium) grows <strong>outside the uterus</strong> — typically on the ovaries, fallopian tubes, the lining of the pelvis, and sometimes the bowel or bladder. Each month, this tissue responds to hormonal changes, thickening and bleeding — but with nowhere to go, it causes inflammation, scar tissue, and adhesions.</p>
<h2 id="symptoms">Recognising the Symptoms</h2>
<ul>
  <li><strong>Dysmenorrhoea:</strong> Severe, debilitating menstrual cramps that may worsen over time</li>
  <li><strong>Chronic pelvic pain:</strong> Persistent pain throughout the month, not just during periods</li>
  <li><strong>Dyspareunia:</strong> Deep pain during or after sexual intercourse</li>
  <li><strong>Painful bowel movements or urination</strong> — especially during menstruation</li>
  <li><strong>Heavy menstrual bleeding</strong> or bleeding between periods</li>
  <li><strong>Infertility:</strong> Found incidentally in 20–40% of infertile women</li>
  <li><strong>Fatigue, bloating, nausea</strong> — often worse around menstruation</li>
</ul>
<h2 id="stages">The 4 Stages of Endometriosis</h2>
<ul>
  <li><strong>Stage I (Minimal):</strong> Small, isolated implants on the pelvic lining with no scar tissue</li>
  <li><strong>Stage II (Mild):</strong> Small implants on the ovaries and pelvic lining; minor scar tissue</li>
  <li><strong>Stage III (Moderate):</strong> Deep implants, small endometriomas on ovaries, more scar tissue</li>
  <li><strong>Stage IV (Severe):</strong> Large ovarian cysts (endometriomas), extensive adhesions binding organs together</li>
</ul>
<h2 id="diagnosis">How Is Endometriosis Diagnosed?</h2>
<p>The gold standard for diagnosis is <strong>diagnostic laparoscopy</strong> — a keyhole procedure that allows Dr. Ankita to directly visualise and biopsy endometrial lesions. Pelvic ultrasound can detect endometriomas but may miss superficial lesions. MRI provides additional detail for deeper infiltration.</p>
<h2 id="treatment">Treatment Options</h2>
<ul>
  <li><strong>Hormonal therapy:</strong> Oral contraceptives, progestogens, or GnRH agonists to suppress the menstrual cycle and slow disease progression</li>
  <li><strong>Laparoscopic excision:</strong> Surgical removal of all visible endometrial tissue — the most effective treatment for pain relief and fertility restoration</li>
  <li><strong>Hysterectomy:</strong> Reserved for severe, treatment-resistant cases in women who have completed their families</li>
</ul>
<h2 id="fertility">Endometriosis and Fertility</h2>
<p>Stage III–IV endometriosis significantly impairs fertility through adhesions, endometriomas, and tubal damage. However, laparoscopic surgery dramatically improves natural conception rates. Dr. Ankita tailors every surgical plan to maximise the chance of future pregnancy wherever desired.</p>';
