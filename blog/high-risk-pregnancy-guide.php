<p>A pregnancy labelled "high-risk" can feel overwhelming and frightening. But being high-risk simply means that you and your baby require <strong>closer monitoring and specialist care</strong> — not that something will inevitably go wrong. With the right support, the vast majority of high-risk pregnancies result in healthy mothers and babies.</p>
<div class="post-callout"><p>"Being called high-risk does not mean your pregnancy is doomed. It means we need to pay closer attention — and that is exactly what we do." — Dr. Ankita Bansal Goyal</p></div>
<h2 id="what-is-high-risk">What Makes a Pregnancy High-Risk?</h2>
<p>A pregnancy is classified as high-risk when there are factors — related to the mother, the baby, or both — that increase the chance of complications before, during, or after delivery.</p>
<h2 id="causes">Common Causes of High-Risk Pregnancy</h2>
<ul>
  <li><strong>Pre-existing medical conditions:</strong> Diabetes, hypertension, thyroid disorders, autoimmune diseases, kidney disease, or heart conditions</li>
  <li><strong>Pregnancy-related conditions:</strong> Gestational diabetes, preeclampsia, placenta previa, or low amniotic fluid</li>
  <li><strong>Multiple pregnancies:</strong> Twins, triplets, or more significantly increase risk of preterm birth and complications</li>
  <li><strong>Age factors:</strong> Mothers under 17 or over 35 face higher risks of chromosomal abnormalities and gestational complications</li>
  <li><strong>Previous pregnancy complications:</strong> Prior preterm birth, miscarriage, or caesarean section</li>
  <li><strong>Lifestyle factors:</strong> Smoking, alcohol, or obesity can all elevate risk</li>
</ul>
<h2 id="monitoring">What Extra Monitoring to Expect</h2>
<ul>
  <li>More frequent prenatal visits — often every 2 weeks from mid-pregnancy, then weekly near term</li>
  <li>Additional ultrasounds to monitor foetal growth and placenta position</li>
  <li>Non-stress tests (NST) and biophysical profiles to check baby's wellbeing</li>
  <li>Detailed blood and urine tests to screen for preeclampsia, anaemia, and infection</li>
  <li>Foetal echocardiography if a heart defect is suspected</li>
</ul>
<h2 id="warning-signs">Warning Signs You Should Never Ignore</h2>
<ul>
  <li>Severe headache or visual disturbances (could indicate preeclampsia)</li>
  <li>Sudden swelling of the face, hands, or feet</li>
  <li>Decreased foetal movement for more than 12 hours</li>
  <li>Vaginal bleeding at any stage</li>
  <li>Abdominal pain or tightening before 37 weeks</li>
  <li>Fever above 38°C</li>
</ul>
<h2 id="delivery">Planning for Safe Delivery</h2>
<p>High-risk pregnancies often require a carefully planned delivery — whether vaginal or caesarean — with a specialist obstetrician present. Dr. Ankita coordinates all aspects of care from diagnosis through delivery and postpartum recovery, ensuring both mother and baby receive the safest possible outcome.</p>';
