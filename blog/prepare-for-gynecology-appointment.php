<p>Visiting a gynaecologist for the first time — or returning after a long gap — can feel daunting. But a gynaecology appointment is one of the most important investments you can make in your long-term health. <strong>Being prepared makes the experience far more comfortable and productive.</strong></p>
<div class="post-callout"><p>"There are no silly questions in my clinic. My patients\' comfort and confidence matter just as much as their physical health. Come with your concerns, and we will work through them together." — Dr. Ankita Bansal Goyal</p></div>
<h2 id="before-visit">Before Your Visit: What to Prepare</h2>
<ul>
  <li><strong>Track your periods:</strong> Note the start and end dates of your last 2–3 menstrual cycles, their heaviness, and any irregularities</li>
  <li><strong>List your medications:</strong> Include all prescription drugs, over-the-counter medicines, vitamins, and supplements</li>
  <li><strong>Document your symptoms:</strong> Write down everything — even symptoms that seem embarrassing or minor. Nothing is too small to mention.</li>
  <li><strong>Gather your medical history:</strong> Previous surgeries, pregnancies, STI testing, Pap smear results, and family history of PCOS, fibroids, or cancer</li>
  <li><strong>Note your contraception:</strong> Current method and any concerns about it</li>
</ul>
<h2 id="questions">Questions to Ask Your Doctor</h2>
<ul>
  <li>Are my periods normal for my age and stage of life?</li>
  <li>Should I have a Pap smear or any other screening today?</li>
  <li>Is there anything in my family history I should be concerned about?</li>
  <li>What lifestyle changes would most benefit my reproductive health?</li>
  <li>Which symptoms should prompt an urgent visit between appointments?</li>
  <li>Are my current contraceptive choices still appropriate?</li>
</ul>
<h2 id="what-to-expect">What to Expect During the Appointment</h2>
<p>Your gynaecologist will begin with a thorough <strong>medical and menstrual history</strong>. Depending on your age, symptoms, and last examination, she may perform:</p>
<ul>
  <li>A <strong>general physical examination</strong> including blood pressure and BMI</li>
  <li>A <strong>pelvic examination</strong> — external and internal assessment of the reproductive organs</li>
  <li>A <strong>Pap smear (cervical screening)</strong> to check for abnormal cells</li>
  <li>A <strong>pelvic or transvaginal ultrasound</strong> if a structural issue is suspected</li>
</ul>
<h2 id="screening">Routine Screening Recommendations</h2>
<ul>
  <li><strong>Pap smear:</strong> Every 3 years from age 21 (or sooner if sexually active)</li>
  <li><strong>STI screening:</strong> As appropriate based on sexual history</li>
  <li><strong>Breast examination:</strong> Annual from age 25; mammogram from 40</li>
  <li><strong>Bone density:</strong> If perimenopausal symptoms are present</li>
</ul>
<h2 id="privacy">Privacy, Comfort & Your Rights</h2>
<p>Everything you share with your doctor is completely <strong>confidential</strong>. You have the right to ask for a female doctor, request a chaperone, pause or stop any examination at any time, and ask for clear explanations before any procedure. Dr. Ankita creates a warm, respectful, judgment-free environment for every patient.</p>';
