<p>Polycystic Ovary Syndrome (PCOS) is one of the most common hormonal disorders affecting women of reproductive age, impacting approximately <strong>1 in 10 women</strong> worldwide. Despite its prevalence, it remains widely misunderstood and is often left undiagnosed for years.</p>
<div class="post-callout"><p>"PCOS is not a life sentence. With the right diagnosis, lifestyle adjustments, and medical support, most women with PCOS lead completely healthy, fulfilling lives." — Dr. Ankita Bansal Goyal</p></div>
<h2 id="what-is-pcos">What Is PCOS?</h2>
<p>PCOS is a hormonal imbalance where the ovaries produce an excess of androgens (male hormones), which interferes with the development and release of eggs during ovulation. The name "polycystic" refers to the appearance of many small, fluid-filled follicles on the ovaries visible on ultrasound.</p>
<h2 id="symptoms">Common Symptoms of PCOS</h2>
<ul>
  <li><strong>Irregular or missed periods</strong> — cycles longer than 35 days, or fewer than 8 per year</li>
  <li><strong>Excess facial or body hair (hirsutism)</strong> — especially on the chin, chest, or abdomen</li>
  <li><strong>Acne and persistently oily skin</strong> — often on the face, chest, and upper back</li>
  <li><strong>Weight gain</strong> — particularly around the abdomen, often resistant to diet and exercise</li>
  <li><strong>Thinning hair or scalp hair loss</strong> — similar to male-pattern baldness</li>
  <li><strong>Difficulty conceiving</strong> — due to irregular or absent ovulation</li>
</ul>
<h2 id="causes">What Causes PCOS?</h2>
<ul>
  <li><strong>Insulin resistance:</strong> Up to 70% of women with PCOS have insulin resistance, triggering excess androgen production.</li>
  <li><strong>Low-grade inflammation:</strong> Chronic inflammation stimulates androgen overproduction in the ovaries.</li>
  <li><strong>Genetics:</strong> PCOS runs in families — your risk is significantly higher if your mother or sister has it.</li>
</ul>
<h2 id="diagnosis">How Is PCOS Diagnosed?</h2>
<p>Diagnosis follows the <strong>Rotterdam Criteria</strong> — at least two of: irregular ovulation, high androgens, or polycystic ovaries on ultrasound. Dr. Ankita conducts a full hormone panel and pelvic ultrasound to confirm.</p>
<h2 id="treatment">Treatment Options for PCOS</h2>
<ul>
  <li><strong>Lifestyle changes:</strong> Even 5–10% weight loss significantly restores hormonal balance.</li>
  <li><strong>Combined oral contraceptive pills:</strong> Regulate periods, reduce androgens, control acne.</li>
  <li><strong>Metformin:</strong> Improves insulin sensitivity and helps restore ovulation.</li>
  <li><strong>Letrozole / Clomiphene:</strong> Fertility medications that stimulate ovulation.</li>
  <li><strong>Laparoscopic ovarian drilling:</strong> Minor surgery that destroys androgen-producing tissue when medications fail.</li>
</ul>
<h2 id="fertility">PCOS and Fertility</h2>
<p>PCOS is the most common cause of anovulatory infertility, but also one of the most treatable. The majority of women with PCOS who want to conceive are able to do so with proper management and support from a specialist.</p>';
